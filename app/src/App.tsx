/** Call Dicta Nakdan API (proxied via Vite dev server) to add niqqud */
async function fetchNikud(rawText: string): Promise<string> {
  const res = await fetch('/nakdan', {
    method: 'POST',
    headers: { 'Content-Type': 'application/json; charset=utf-8' },
    body: JSON.stringify({ task: 'nakdan', data: rawText, genre: 'modern', addmorph: false, keepqq: false }),
  });
  if (!res.ok) throw new Error(`HTTP ${res.status}`);
  const data = await res.json();
  if (!Array.isArray(data)) throw new Error('Unexpected response');
  // Each item: { word, sep, options: [nikud1, nikud2,...], fpasuk }
  // sep:true = space/punctuation → use word as-is
  // sep:false = content word → use options[0] (best nikud guess)
  return data.map((item: { word: string; sep: boolean; options: string[] }) => {
    if (item.sep) return item.word;                              // space or punctuation
    if (item.options?.length) return item.options[0].replace(/\|/g, ''); // best niqqud, strip morpheme separators
    return item.word;                                            // fallback: original
  }).join('');
}

