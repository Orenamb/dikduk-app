from docx.enum.text import WD_ALIGN_PARAGRAPH
from docx.shared import Pt

doc = docx.Document()

def add_rtl_p(text, is_heading=False, level=1):
    if is_heading:
        p = doc.add_heading(text, level=level)
    else:
        p = doc.add_paragraph()
        p.add_run(text)
    p.alignment = WD_ALIGN_PARAGRAPH.RIGHT

